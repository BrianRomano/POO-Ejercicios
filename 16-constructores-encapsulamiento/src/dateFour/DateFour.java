package dateFour;

public class DateFour {
    private int day;
    private int month;
    private int year;

    //Constructor por defecto
    public DateFour(){}
    //Constructor sobrecargado
    public DateFour(int day, int month, int year){
        this.day = day;
        this.month = month;
        this.year = year;
    }
}
